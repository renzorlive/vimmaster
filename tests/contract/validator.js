const rules = [];

/**
 * Registers a new validation rule for lessons.
 * @param {Object} rule
 * @param {string} rule.id - e.g., 'L001'
 * @param {string} rule.name - Human readable name
 * @param {string} rule.severity - 'error', 'warning', 'info'
 * @param {Function} rule.validate - Function that takes (lesson, report)
 */
export function registerRule(rule) {
    if (!rule.id || !rule.name || !rule.severity || typeof rule.validate !== 'function') {
        throw new Error('Invalid rule registered');
    }
    rules.push(rule);
}

/**
 * Validates a lesson against all registered rules.
 * @param {Object} lesson - The lesson data object
 * @param {string|number} fallbackId - Identifier to use if lesson.id is missing
 * @returns {Array} List of ValidationError objects
 */
export function validateLesson(lesson, fallbackId) {
    const errors = [];
    const lessonId = lesson.id || `Lesson ${fallbackId}`;

    // The report function passed to rules
    const report = (message, field = null, suggestion = null, overrideSeverity = null) => {
        errors.push({
            lessonId,
            ruleId: null, // Set dynamically by the runner loop
            severity: overrideSeverity, // Set dynamically
            field,
            message,
            suggestion
        });
    };

    for (const rule of rules) {
        // Collect errors generated by this rule
        const ruleErrorsBefore = errors.length;
        
        try {
            rule.validate(lesson, report);
        } catch (e) {
            report(`Rule execution crashed: ${e.message}`);
        }

        // Tag all new errors with the rule's ID and severity
        for (let i = ruleErrorsBefore; i < errors.length; i++) {
            errors[i].ruleId = errors[i].ruleId || rule.id;
            errors[i].severity = errors[i].severity || rule.severity;
        }
    }

    return errors;
}

export function getRegisteredRules() {
    return [...rules];
}
