import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

// Import the validator and rules
import { validateLesson as runValidator } from '../tests/contract/validator.js';
import '../tests/contract/rules/id.js';
import '../tests/contract/rules/cursor.js';
import '../tests/contract/rules/buffer.js';
import '../tests/contract/rules/solution.js';
import '../tests/contract/rules/objective.js';
import '../tests/contract/rules/unknown-fields.js';
import '../tests/contract/rules/metadata.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const CONTENT_DIR = path.join(__dirname, '../content/lessons');
const OUTPUT_JS = path.join(__dirname, '../js/generated-content.js');
const OUTPUT_INDEX = path.join(__dirname, '../content/index.json');

let hasErrors = false;

function discoverLessons() {
    if (!fs.existsSync(CONTENT_DIR)) return [];
    return fs.readdirSync(CONTENT_DIR)
        .filter(f => f.endsWith('.json'))
        .map(f => path.join(CONTENT_DIR, f));
}

function loadLesson(filePath) {
    const raw = fs.readFileSync(filePath, 'utf8');
    return JSON.parse(raw);
}

function validateLesson(lesson) {
    const errors = runValidator(lesson, lesson.id || 'unknown');
    let lessonHasErrors = false;
    for (const e of errors) {
        if (e.severity === 'error') {
            console.error(`\x1b[31m[ERROR]\x1b[0m ${lesson.id} - ${e.ruleId}: ${e.message} ${e.suggestion || ''}`);
            lessonHasErrors = true;
            hasErrors = true;
        } else if (e.severity === 'warning') {
            console.warn(`\x1b[33m[WARN]\x1b[0m ${lesson.id} - ${e.ruleId}: ${e.message}`);
        }
    }
    return !lessonHasErrors;
}

function normalizeLesson(rawLesson) {
    const lesson = { ...rawLesson };
    if (!lesson.metadata) lesson.metadata = {};
    const validDifficulties = ['beginner', 'intermediate', 'advanced'];
    if (!lesson.metadata.difficulty || !validDifficulties.includes(lesson.metadata.difficulty)) {
        lesson.metadata.difficulty = 'beginner';
    }
    if (!lesson.metadata.prerequisites) {
        lesson.metadata.prerequisites = [];
    }
    return lesson;
}

function bundleLessons(normalizedLessons) {
    const index = {
        version: 1,
        contentVersion: 1,
        generatedAt: new Date().toISOString(),
        generator: 'scripts/build-content.js',
        // In the migration, we will split them correctly. For now, we put everything into regularLessons 
        // unless it has a tag "practice", but we'll sort that out when migrating.
        regularLessons: [],
        practiceLessons: []
    };
    
    const lessonsDict = {};

    // Sort the lessons by their metadata.order (defaulting to 999 if missing)
    normalizedLessons.sort((a, b) => {
        const orderA = a.metadata && a.metadata.order !== undefined ? a.metadata.order : 999;
        const orderB = b.metadata && b.metadata.order !== undefined ? b.metadata.order : 999;
        return orderA - orderB;
    });

    for (const lesson of normalizedLessons) {
        lessonsDict[lesson.id] = lesson;
        // Temporary logic for splitting, assuming we tag cheat-mode as "practice"
        if (lesson.metadata && lesson.metadata.tags && lesson.metadata.tags.includes('practice')) {
            index.practiceLessons.push(lesson.id);
        } else {
            index.regularLessons.push(lesson.id);
        }
    }

    return { index, lessons: lessonsDict };
}

function writeArtifact(bundle) {
    // Write content/index.json
    fs.writeFileSync(OUTPUT_INDEX, JSON.stringify(bundle.index, null, 2), 'utf8');

    // Write js/generated-content.js
    const jsContent = `/**
 * AUTO-GENERATED.
 * DO NOT EDIT.
 *
 * Generated by:
 * scripts/build-content.js
 */
export const generatedContent = ${JSON.stringify(bundle, null, 2)};
`;
    fs.writeFileSync(OUTPUT_JS, jsContent, 'utf8');
}

function main() {
    console.log('Building content artifacts...');
    
    const files = discoverLessons();
    const normalizedLessons = [];

    for (const file of files) {
        const raw = loadLesson(file);
        
        // Exclude the schema example from being bundled as a real level
        if (raw.id === 'lesson-example-movement') continue;

        if (validateLesson(raw)) {
            normalizedLessons.push(normalizeLesson(raw));
        }
    }

    if (hasErrors) {
        console.error('\x1b[31mBuild failed due to validation errors.\x1b[0m');
        process.exit(1);
    }

    const bundle = bundleLessons(normalizedLessons);
    writeArtifact(bundle);

    console.log(`\x1b[32mSuccessfully bundled ${normalizedLessons.length} lessons.\x1b[0m`);
}

main();
